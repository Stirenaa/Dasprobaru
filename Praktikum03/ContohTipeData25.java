public class ContohTipeData25 {
    public static void main(String[] args) {
        char GolonganDarah = 'A';
        short JumlahPenduduk = 2700;
        float suhu = 60.50f;
        double berat = 58.45;
        long saldo = 18000;
        int angkaDesimal = 0x10;

        System.out.println("GolonganDarah\t: " + (byte)GolonganDarah);
        System.out.println("JumlahPenduduk\t: " + JumlahPenduduk);
        System.out.println("Suhu\t\t: " + suhu);
        System.out.println("Berat\t\t: " + (float)berat);
        System.out.println("Saldo\t\t: " + saldo);
        System.out.println("Angka Desimal\t: " + angkaDesimal);



    }
}
